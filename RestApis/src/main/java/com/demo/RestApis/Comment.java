package com.demo.RestApis;

public class Comment {
    private Long id;
    private String comment;

    // Getters and Setters
    public Long getId() {
        return id;
    }

    
    public void setId(Long id) {
        this.id = id;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
}
